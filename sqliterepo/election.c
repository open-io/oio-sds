#define EVENTLOG_SIZE 16
#define STATUS_FINAL(e) ((e) >= STEP_LOST)

typedef guint req_id_t;

enum sqlx_action_e
{
	ACTION_NONE,
	ACTION_PING,
	ACTION_FAIL,
	ACTION_RETRY, /* re-send getvers */
	ACTION_RESTART,
	ACTION_LEAVE,
	ACTION_EXPIRE
};

enum election_step_e
{
	STEP_NONE = 0,
	STEP_CANDREQ,
	STEP_CANDOK,
	STEP_LEAVING,
	STEP_PRELOST,
	STEP_PRELEAD,
	STEP_LOST,
	STEP_LEADER,
	STEP_FAILED,
#define STEP_MAX (STEP_FAILED+1)
};

enum event_type_e
{
	EVT_NONE = 0,
	EVT_DISCONNECTED,
	EVT_EXITING,

	EVT_GETVERS_OK,
	EVT_GETVERS_OUTDATED,
	EVT_GETVERS_CONCURRENT,
	EVT_GETVERS_ERROR,

	EVT_CREATE_OK,
	EVT_CREATE_KO,

	EVT_NODE_LEFT,

	EVT_MASTER_KO,
	EVT_MASTER_EMPTY,
	EVT_MASTER_OK,
	EVT_MASTER_CHANGE,

	EVT_RESYNC_REQ,
	EVT_RESYNC_DONE,

	EVT_LIST_OK,
	EVT_LIST_KO,

	EVT_LEAVE_OK,
	EVT_LEAVE_KO,
};

